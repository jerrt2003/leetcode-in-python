class Solution(object):
    def numIslands(self, grid):
        """
        :type grid: List[List[str]]
        :rtype: int
        """
        self.res = 0
        self.visited = set()
        self.m = len(grid)
        self.n = len(grid[0])
        self.grid = grid
        for i in range(self.m):
            for j in range(self.n):
                if (i, j) not in self.visited and self.grid[i][j] == '1':
                    self.res += 1
                    self.dfs(i, j)
        return self.res

    def dfs(self, i, j):
        self.visited.add((i, j))
        for x, y in [(i - 1, j), (i + 1, j), (i, j - 1), (i, j + 1)]:
            if 0 <= x < self.m and 0 <= y < self.n and self.grid[x][y] == '1' and (x, y) not in self.visited:
                self.dfs(x, y)
        return

