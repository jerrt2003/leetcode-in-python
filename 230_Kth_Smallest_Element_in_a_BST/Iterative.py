# Definition for a binary tree node.
# class TreeNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution(object):
    def kthSmallest(self, root, k):
        """
        Facebook
        Stack
        Iterative
        T:O(n) S:O(n)
        Iterative: FILO
        Perf: Runtime: 44 ms, faster than 81.37% / Memory Usage: 19.6 MB, less than 54.19%
        :type root: TreeNode
        :type k: int
        :rtype: int
        """
        stack = []
        while True:
            while root:
                stack.append(root)
                root = root.left
            root = stack.pop()
            k -= 1
            if k == 0:
                return root.val
            root = root.right